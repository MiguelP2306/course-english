/*

  Subject Pronouns = pronombre sujeto
  We use Subject Pronouns when we refer to a person, animal or thing that does the action

  i, you, he, she, it, we, you, they


  Object Pronouns = pronombre objecto
  Object pronouns receive the action, these are the person, the animal, the object, the place that receive the action

  me, you, him, her, it, us, them


  i do the action or the action is for me
  you perfom the action or the action is for you
  he does the action or this is for him
  she does the action or the action is for her
  it does the action or the action is for it
  we do the action or the action is for us
  they do the action or the action is for them
*/